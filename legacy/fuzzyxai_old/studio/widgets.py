from __future__ import annotations


def explain_action_text(action: str, *, chi_r_crit: int, chi_auto: bool, rho: float) -> str:
    action = str(action)
    if chi_r_crit == 1:
        return (
            'Решение заблокировано, потому что найден критический разрыв объяснительной траектории. '
            'Автоматическое применение запрещено.'
        )
    if not chi_auto:
        return 'Контекст запрещает автоматическое применение. Решение переводится в ручной/аудитный контур.'
    mapping = {
        'accept': 'Автоприменение разрешено: риск низкий, ограничения безопасности выполнены.',
        'lower_confidence': 'Результат допускается только со сниженной уверенностью.',
        'request_more_data': 'Нужно больше данных: текущих сигналов недостаточно для безопасной автоматизации.',
        'defer_to_human': 'Решение передано эксперту из-за повышенного риска/неопределённости.',
        'block': f'Автоприменение заблокировано: риск {rho:.3f} слишком высокий.',
    }
    return mapping.get(action, 'Решение получено по текущей политике наблюдателя.')
